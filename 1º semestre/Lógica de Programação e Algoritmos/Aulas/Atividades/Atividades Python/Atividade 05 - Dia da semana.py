inf = input ("Digite um número")
inf = float(inf)

if (inf == 1):
    print ("Domingo")
elif (inf == 2):
    print ("Segunda - Feira")
elif (inf == 3):
    print ("Terça - Feira")
elif (inf == 4):
    print ("Quarta - Feira")
elif (inf == 5):
    print ("Quinta - Feira")
elif (inf == 6):
    print ("Sexta - Feira")
elif (inf == 7):
    print ("Sábado")

else:
    print("Digite um número")